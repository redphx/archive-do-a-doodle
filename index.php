			<b>Cài đặt :</b><br />
			- Chạy file <b>do-a-doodle.exe</b> vừa download ở trên<br />
			- Trong mục <b>Destination Folder</b>, chỉ đến đường dẫn nơi cài đặt <b>Yahoo! Messenger</b>, thường là <b>C:\Program Files\Yahoo!\Messenger</b><br />
			- Nhấn <b>Install</b><br />
			- Nhấn vào <a href="ymsgr:getimv?doodle"><b>đây</b></a>, hoặc mở một khung chat bất kỳ, chọn <b>IMVironment</b> ~> <b>See all IMVironment</b> ~> <b>Interactive Fun</b> ~> <b>Doodle</b><br />
			- Nếu phía góc dưới bên phải có chữ <b>Do-A-Doodle</b> thì việc cài đặt đã thành công :)<br /><br />
			<b>Sử dụng :</b><br />
			- Vào <a href="http://doodle.huhiho.com" target="_blank"><b>http://doodle.huhiho.com</b></a> để tạo line ( khung phía trên )<br />
			- Đặt ảnh vào mục <b>Ảnh cần vẽ</b> và nhấn <b>Vẽ</b>. Chờ một chút sẽ hiện ra file download<br />
			- Lưu file ( ví dụ có tên <b>dog.txt</b> ) vào thư mục <b>imvcache\doodle\lines</b> trong thư mục cài đặt <b>Yahoo! Messenger</b><br />
			- Mở file <b>config.txt</b> ( trong <b>imvcache\doodle</b> ), gõ nội dung <b>file=TÊN_FILE_VỪA_TẠO</b>, ví dụ <b>file=dog.txt</b>, rồi <b>Save</b> lại<br />
			- Quay trở lại <b>Do-A-Doodle</b>, nhấn <b>Reload Picture</b><br />
			- Nhấn <b>Start Drawing</b>, <b>Pause Drawing</b> hay <b>Stop Drawing</b> để điều khiển quá trình vẽ<br /><br />
